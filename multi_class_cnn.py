# Import libraries
import numpy as np
import matplotlib.pyplot as plt
from tensorflow.keras.preprocessing import image
from keras.models import load_model

# Load the trained model
model = load_model('best_model.keras')

# Load class names from training generator (you must define this once during training)
#class_names = list(train_generator.class_indices.keys())
class_names = ['airplane', 'automobile', 'bird', 'cat', 'deer', 'dog', 'frog', 'horse', 'ship', 'truck']


def predict_image(image_path):
    # Load and preprocess image
    img = image.load_img(image_path, target_size=(32, 32))
    img_array = image.img_to_array(img)
    img_array = img_array / 255.0
    img_batch = np.expand_dims(img_array, axis=0)

    # Predict
    result = model.predict(img_batch)
    predicted_class = np.argmax(result)
    confidence = round(result[0][predicted_class] * 100, 2)
    prediction = class_names[predicted_class]

    # Print prediction
    print(f"\n🖼️ Image: {image_path}")
    print(f"Predicted Class: {prediction}, Confidence: {confidence}%")

    print("\nClass Probabilities:")
    for i, class_name in enumerate(class_names):
        prob = round(result[0][i] * 100, 2)
        print(f"{class_name}: {prob}%")

    # Display image with smaller size
    plt.figure(figsize=(1, 1))  # Smaller display size
    plt.imshow(img)
    plt.title(f"{prediction} ({confidence}%)")
    plt.axis('off')
    plt.show()


